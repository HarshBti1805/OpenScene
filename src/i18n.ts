let catalog = en;

export function t(key: string, vars?: Vars): string {
  let msg = catalog[key];
  if (msg === undefined) return key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      msg = msg.split(`{${k}}`).join(String(v));
    }
  }
  return msg;
}

/** Swap the active catalog (future locales merge over English). */
export function setCatalog(overrides: Record<string, string>) {
  catalog = { ...en, ...overrides };
}
